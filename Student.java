package thinkQ;


public class Student {

	
		int Score;
		String Name;
		public Student(int score, String name) {
			super();
			Score = score;
			Name = name;
		}
		public int getScore() {
			return Score;
		}
		public void setScore(int score) {
			Score = score;
		}
		public String getName() {
			return Name;
		}
		public void setName(String name) {
			Name = name;
		}
		
		
		

	}


